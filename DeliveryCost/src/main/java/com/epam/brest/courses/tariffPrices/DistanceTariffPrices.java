package com.epam.brest.courses.tariffPrices;

public class DistanceTariffPrices {

    private double minDistanceTariff;
    private double averageDistanceTariff;
    private double maxDistanceTariff;

    public void setMinDistanceTariff(double minDistanceTariff) {
        this.minDistanceTariff = minDistanceTariff;
    }

    public double getMinDistanceTariff() {
        return minDistanceTariff;
    }

    public void setAverageDistanceTariff(double averageDistanceTariff) {
        this.averageDistanceTariff = averageDistanceTariff;
    }

    public double getAverageDistanceTariff() {
        return averageDistanceTariff;
    }

    public void setMaxDistanceTariff(double maxDistanceTariff) {
        this.maxDistanceTariff = maxDistanceTariff;
    }

    public double getMaxDistanceTariff() {
        return maxDistanceTariff;
    }
}
