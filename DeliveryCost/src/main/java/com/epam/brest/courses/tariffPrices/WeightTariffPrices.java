package com.epam.brest.courses.tariffPrices;

public class WeightTariffPrices {

    private double minWeightTariff;
    private double averageWeightTariff;
    private double maxWeightTariff;

    public void setMinWeightTariff(double minWeightTariff) {
        this.minWeightTariff = minWeightTariff;
    }

    public double getMinWeightTariff() {
        return minWeightTariff;
    }

    public void setAverageWeightTariff(double averageWeightTariff) {
        this.averageWeightTariff = averageWeightTariff;
    }

    public double getAverageWeightTariff() {
        return averageWeightTariff;
    }

    public void setMaxWeightTariff(double maxWeightTariff) {
        this.maxWeightTariff = maxWeightTariff;
    }

    public double getMaxWeightTariff() {
        return maxWeightTariff;
    }
}
